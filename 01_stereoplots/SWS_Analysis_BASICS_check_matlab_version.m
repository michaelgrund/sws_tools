function vers_out=SWS_Analysis_BASICS_check_matlab_version()
%==========================================================================


%==================================================================================================================================
%==================================================================================================================================

vers=version('-release');

vers_yyyy=str2double(vers(1:4));
vers_let=vers(5);

if vers_yyyy > 2016 || (vers_yyyy == 2016 && strcmp(vers_let,'b')) % MATLAB 2016b or higher
   vers_out=1;
else
    vers_out=0;
end

%==================================================================================================================================
%==================================================================================================================================
% EOF